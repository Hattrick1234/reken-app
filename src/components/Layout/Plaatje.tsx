type PropsPlaatje = {
  typePlaatje: TypePlaatje;
};

const Plaatje = (props: PropsPlaatje) => {
  let altValue = "";
  let PlaatjesArrayVoorDitAntwoord = [];
  switch (props.typePlaatje) {
    case TypePlaatje.GOED: {
      altValue = "goed antwoord";
      PlaatjesArrayVoorDitAntwoord = PlaatjesVoorGoedeAntwoorden;
      break;
    }
    case TypePlaatje.FOUT: {
      altValue = "fout antwoord";
      PlaatjesArrayVoorDitAntwoord = PlaatjesVoorFouteAntwoorden;
      break;
    }
    case TypePlaatje.DOELBEREIKT: {
      altValue = "doel bereikt";
      PlaatjesArrayVoorDitAntwoord = PlaatjesVoorDoelBereikt;
      break;
    }
    case TypePlaatje.TIJDVERSTREKEN: {
      altValue = "tijd verstreken";
      PlaatjesArrayVoorDitAntwoord = PlaatjesVoorVerstrekenTijd;
      break;
    }
  }

  return (
    <StyledImg
      src={
        PlaatjesArrayVoorDitAntwoord[
          Math.floor(Math.random() * PlaatjesArrayVoorDitAntwoord.length)
        ]
      }
      alt={altValue}
    />
  );
};

